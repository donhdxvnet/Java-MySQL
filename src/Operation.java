public class Operation
{	
	private String nom = null;
	private String date = null;
	
	public void setNom(String nom)
	{
		this.nom = nom;
	}
	
	public void setDate(String date)
	{
		this.date = date;
	}
	
	public String getNom()
	{
		return this.nom;
	}
	
	public String getDate()
	{
		return this.date;
	}
}